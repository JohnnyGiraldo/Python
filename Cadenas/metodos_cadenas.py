# Metodos de cadenas

cadena1 = "Hola Mundo"
print(f"Cadena Original: {cadena1}")
mayusculas = cadena1.upper()  # Metodo upper para convertir a mayusculas
print(f"Cadena en mayusculas: {mayusculas}")
print(f"Cadena en minusculas: {cadena1.lower()}")# metodo lower para convertir a minusculas
cadena2 = "Juan perez"
print(f"Cadena con espacios: {cadena2}")
print(f"cadena sin espacios: {cadena2.strip()}") # Elimina espacios en blanco
