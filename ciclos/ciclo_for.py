print('*** Ciclo For ***')
print('Recorremos los caracteres de una cadena:')
cadena = 'Hola Mundo'
# Iteramos los caracteres
for letra in cadena:
    print(letra, end=' ')

print('\n\nRecorremos la lista de frutas:')

frutas = ['platano','fresa','Mango']
for fruta in frutas:
    print(fruta, end=' ')