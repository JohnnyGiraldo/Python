print(f"***  Operadores de comparacion  *** ")
a, b = 7, 5
print(f"valor inicial a: {a}, Valor de b: {b}")

# Operador de Igualdad
resultado = a == b
print(f"Resultado de a == b es: {resultado}")

# Operador Diferente  !=
resultado = a != b
print(f" Resultado de a != b es : {resultado}")

# Operador mayor que >
resultado = a > b
print(f"Resultado de a > b  es: {resultado}")

# Operador de mayor o igual que >=
resultado = a >= b
print(f"Resultado de a > = b es : {resultado}")

# Operador de menor que <
resultado = a < b
print(f"Resultado de a < b es: {resultado}")

# Operador de menor o igual que <=
resultado = a <= b
print(f"Resultado de a <= b es: {resultado}")