print(f"***   OPERADORES DE ASIGNACION COMPUESTOS  ***")
a, b = 10, 15
print(f"Valor Inicial de a:{a}, b:{b}")

# Operador Compuesto de Suma +=
a += b  # a = a + b
print(f"Operador a += b es :{a}")

# Operador Compuesto de resta -=
a = 10
# Reiniciamos la variable a
a -= b # a = a - b
print(f"Operador a -= b es : {a}")

# Operador Compuesto de Multiplicacion *=
a = 10
#Reiniciamos el valor de a
a *= b
print(f"Operador de a *= es: {a}")

# Operador Compuesto de division  /=
a = 10
#Reiniciamos el valor de a
a /= b  # a = a / b
print(f"operador a /= b es: {a:.2f}")
