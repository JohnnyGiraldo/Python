print("***  EL MAYOR DE DOS NUMEROS  ***")

# Solicitar los valores al usuario
numero1 = int(input("Proporciona el Numero 1: "))
numero2 = int(input("Proporciona el Numero 2: "))

# El mayor de dos numeros
if numero1 > numero2:
    print(f"El Numero 1 es mayor: {numero1}")
else:
    print(f"El Numero 2 es mayor: {numero2}")